                                    <p> Direktorat Jenderal Ketenagalistrikan adalah unsur pelaksana sebagian tugas dan fungsi Kementrian, yang berada di bawah dan bertanggungjawab kepada Menteri Energi dan Sumber Daya Mineral. Direktorat Jenderal Ketenagalistrikan dipimpin oleh Direktur Jenderal. </p>
                                    <p> Sejarah Direktorat Jenderal ketenagalistrikan dengan dimulainya pelaksanaan Pelita I dan terbentuknya kabinet pembangunan I, peningkatan usaha pembangunan negara pun mulai dilaksanakan, dan itu memerlukan daya tangkap yang besar. Dengan demikian pemekaran organisasi Departemen Pertambangan pun tidak dapat dielakkan lagi. Pemekaran organisasi Departemen Pertambangan menjadi Departemen Pertambangan dan Energi sesuai dengan keputusan Presiden Nomor 45 Tahun 1947. </p>
                                    <p> Direktorat Jenderal Ketenagalistrikan pada awalnya bernama Direktorat Jenderal Ketenagaan yang mempunyai tugas melaksanakan sebagian tugas pokok Departemen dibidang Ketenagaan berdasarkan kebijakan yang ditetapkan oleh Menteri Pertambangan dan Energi. Direktorat Jenderal Ketenagaan baru disahkan pada tahun 1978, sesuai dengan keputusan Menteri Pertambangan dan Energi Nomor 734 Tahun 1978.</p>

                                    <p>Pada tahun 1984 Direktorat Jenderal Ketenagaan berganti nama dengan Direktorat Jenderal Listrik dan Energi Baru yang mempunyai tugas melaksanakan sebagian tugas pokok Departemen dibidang Kelistrikan dan Energi Baru. Dengan terbentuknya kabinet pembangunan IV berdasarkan keputusan Presiden Nomor 45 Tahun 1983 Tanggal 16 Maret 1983, maka dengan Keputusan Presiden Nomor 15 Tahun 1984 Tanggal 6 Maret 1984 tentang susunan organisasi Departemen pada Bab IX Direktorat Jenderal Listrik dan Energi Baru untuk memperjelas tugas dan fungsinya.</p>

                                    <p> Kemudian pada tahun 1998, Direktorat Jenderal Listrik dan Energi Baru berubah kembali namanya menjadi Direktorat Jenderal Listrik dan Pengembangan Energi Untuk menyesuaikan tugas dan fungsi dengan kegiatan Direktorat Jenderal, serta guna menunjang pelaksanaan pembangunan maka susunan organisasi Departemen Pertambangan dan Energi di ubah sesuai dengan keputusan Presiden Nomor 48 Tahun 1997 Tanggal 11 November 1997. Dalam rangka pelaksanaan lebih lanjut maka organisasi dan tata kerja Direktorat Jenderal Listrik dan Pengembangan Energi ditata kembali sebagaimana diatur dengan Keputusan Menteri Pertambangan dan Energi Nomor 169 Tahun 17 Februari 1998. Untuk mendukung pelaksanaan tugas kabinet dan meningkatkan penyelenggaraan pemerintah agar dapat berjalan lancar, berdaya guna dan berhasil guna, serta mendukung penyelenggaraan tertib adminstrasi pemerintah, perlu diubah susunan organisasi Departemen pertambangan dan energi. Sesuai dengan keputusan Presiden Nomor 165 Tahun 2000 dan Nomor 177 Tahun 2000 Tanggal 23 November 2000 tentang Organisasi dan Tata Kerja Departemen, Departemen Pertambangan dan Energi diubah namanya menjadi Departemen Energi dan Sumber Daya Mineral dan membawahi Sekretaria Jenderal, Inspektorat Jenderal, 3 (tiga) Direktorat Jenderal, Badan Pendidikan dan Pelatihan, Badan Penelitian dan Pengembangan, dan Pusat Informasi Energi.</p>

                                    <p> Untuk menindaklanjuti Keputusan Presiden di atas, diterbitkan keputusan Menteri Energi dan Sumber Daya Mineral No. 150 tahun 2001 tanggal 2 Maret 2001 tentang Susunan Organisasi dan Tata Kerja, Departemen energi dan Sumber Daya Mineral, dan Direktorat Jenderal Listrik dan Pengembangan energi diubah menjadi Direktorat Jenderal Listrik dan Pemanfaatan Energi, Balai dihilangkan dan Direktorat Pengembangan Energi diubah menjadi Direktorat Energi Baru terbarukan dan Konservasi Energi, sesuai dengan tugas dan fungsinya.</p>

                                    <p>Dan terakhir Sesuai dengan Peraturan Presiden Nomor 24 Tahun 2010, nama Direktorat Jenderal Listrik dan Pemanfaatan Energi dihapus dan diganti dengan Direktorat Jenderal Ketenagalistrikan. Direktorat Jenderal Ketenagalistrikan mempunyai tugas merumuskan serta melaksanakan kebijakan dan standardisasi teknis di bidang ketenagalistrikan. Dalam melaksanakan tugas tersebut, Direktorat Jenderal Ketenagalistrikan mempunyai fungsi : </p>
                                <ol>
                                        <li>Perumusan kebijakan di bidang ketenagalistrikan;</li>
                                        <li>Pelaksanaan kebijakan di bidang ketenagalistrikan;</li>
                                        <li>Penyusunan norma, standar, prosedur, dan kriteria di bidang ketenagalistrikan;</li>
                                        <li>Pemberian bimbingan teknis dan evaluasi di bidang ketenagalistrikan;</li>
                                        <li>Pelaksanaan administrasi Direktorat Jenderal Ketenagalistrikan.</li>
                                    </ol>
